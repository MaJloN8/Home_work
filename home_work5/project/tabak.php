<h1>
Kotlin
</h1>
<p>
<b>Kotlin</b> – структурированный и многоцелевой язык программирования, разработанный компанией JetBrains.
Он разрабатывался как более “компактная” версия Java и вышел легче на 40%. При этом, с Java он полностью совместим и открывается на его виртуальной машине (JVM).
<br><br>
<b>Где используют:</b> мобильная и серверная разработка.
<br><br>
<b>Возможности:</b> на Kotlin можно писать кроссплатформенный код, однако самым популярным направлением его применения остается 
ОС Android – в 2017 году Google провозгласил его официальным языком разработки приложений под ОС на ряду с С++ и Java.
На сегодняшний день – 80% самых популярных приложений в Play Market написаны с помощью <b>Kotlin</b>.
</p>
