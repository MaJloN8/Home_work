<h1>Golang</h1>
<img src="foto66.png"  width="900" href="vh.html?do=logout" height="510" class="krugi" alt="Golang" />
<p>
Golang или Go – компилируемый многопоточный язык программирования от Google. В этом языке нет привычных объектов, классов и наследования,
 как-либо усложняющих код и его изменения, а его официальное руководство расположено всего на 50 страницах!

<b>Изначально Golang планировался как альтернатива С и С++ для внутреннего использования в Google. По итогу, язык собрал в себе еще и лучшие функции Python, Pascal, Oberon и Modula.</b>
<br>
<b>Где используют:</b> консольные приложения, сложные многопоточные программы и даже графические интерфейсы доступны для написания на Go, однако под серверные приложения язык подходит лучше всего.
<br><br>
<b>Особенности:</b> в Go используется упрощенный синтаксис – это позволяет облегчить работу не только самому разработчику, но и его коллегам по команде.
 Параллелизм – другая особенность этого языка, позволяющая выполнять несколько функций одновременно. Стандартная библиотека Go легко читаемая, 
 быстрая и качественная – это открывает дорогу к реализации большого количества задач.
 А если этого мало – существует много других библиотек, которые могут отлично взаимодействовать с библиотеками языка Си.
 </p>
