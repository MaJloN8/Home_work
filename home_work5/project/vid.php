<h1>JavaScript </h1>
<img src="foto55.jpg"  width="250" href="vh.html?do=logout" height="250" class="krugi" alt="JS" align="right"/>
<p>
Язык, который редко выходит из топ-5. <b> JavaScript</b> “оживляет” веб-страницы и является неповторимым для фронтенда в веб-разработке. Все, что сегодня мы видим на странице или, по-другому, на “клиентской стороне” – это JS. Название, к слову, единственное что объединяет этот язык с Java – на заре создания создатели решили таким образом “урвать” небольшой кусочек славы гиганта со схожим названием.

Изначально JavaScript обладал скромными возможностями – с его помощью могли добавить лишь немного кнопок на страницу. Создание модуля Node.js расширило горизонты языка. Однако, веб-страницы остаются его главной “мощью”, только теперь с сильно прокаченным функционалом.
<br><br>
<b>Где используют:</b> веб-страницы, веб-приложения, мобильная разработка, программирование “умных” устройств. Например, Notion или Discord написаны на JS.
<br><br>
<b>Возможности:</b> сегодня JavaScript можно использовать как на клиентской, так и на серверной стороне. Это стало доступно благодаря модулю Node.js, 
который способен работать с базами данных и обрабатывать запросы к серверу. А различные фреймворки расширяют возможности JS. Например React Native, 
который позволяет писать десктопные приложения. Или Electron, 
объединяющий в себе технологии HTML, CSS и JS и позволяющая создавать кроссплатформенные приложения для настольных операционных систем.
</p>
