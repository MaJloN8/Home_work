<h1>Java</h1>
<img src="foto44.jpeg"  width="250" href="vh.html?do=logout" height="250" class="krugi" alt="Java" align="left"/>
<p>
<b>Java</b> – кроссплатформенный объектно-ориентированный язык программирования. Он не теряет своей популярности с момента появления и является основой многих веб-сервисов и Android-приложений.
 Особенно язык популярен у крупных корпораций, где требуется создание высоконагруженных систем, например, в корпоративном ПО. Однако,
 в мобильной разработке Java начинает уступать более современному Kotlin.
<br><br>
<b>Где используют:</b> почти везде: программы, десктопные приложения, веб-приложения и сервера, приложения под Android можно написать на Java.
 Проблемы есть с играми – особенности языка не позволяют сделать необходимую оптимизацию под процессор и видеокарту.
<br><br>
<b>Особенности:</b> Java компилируется не совсем обычным способом – сначала код преобразуется в специальный байт-код, а затем выполняется JVM (Java Virtual Machine).
 Эта особенность делает Java кроссплатформенным языком – программист пишет под JVM, которая дальше адаптирует код под нужную платформу.<br><br>
</p>
